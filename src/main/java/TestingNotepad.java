    public static String openFile(String dir){
        String isi = "";
        try {
            InputStream file = new FileInputStream(dir);    
            int data = file.read();
            while (data != -1) {
                isi += ((char) data);
//                System.out.write(data);
                data = file.read();
            }
            file.close();
//            fileTextArea.setText(isi);
        } catch (IOException e){
            System.out.println(e);
        }
        
        return isi;
    }
    public static void saveEdit(String dir, String data){
        try {
            OutputStream file = new FileOutputStream(dir);
            String save = data;
            file.write(save.getBytes());
            file.flush();
            file.close();
        } catch (IOException e) {
            System.out.println(e);
        }
    }
    
