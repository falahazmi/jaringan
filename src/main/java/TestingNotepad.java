    private void NewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NewActionPerformed
        // TODO add your handling code here:
        if (!"".equals(TextArea.getText())) { //Jika ada isinya, maka muncul prompt.
            int konfirmasi = JOptionPane.showConfirmDialog(null, "Simpan Filenya?", "Konfirmasi New File", JOptionPane.YES_NO_OPTION);
            if (konfirmasi == JOptionPane.YES_OPTION) { // kalau disimpan, melakukan proses save, lalu textarea direfresh dan file global direset.
                String dir;
                if (!"".equals(fileGlobal)) {
                    dir = fileGlobal;
                } else {
                    jFileChooser1.showSaveDialog(null);
                    dir = jFileChooser1.getSelectedFile().getPath();
                    fileGlobal = dir;
                }
                String data = TextArea.getText();
                saveEdit(dir, data);
                
                TextArea.setText("");
                fileGlobal = "";
            } else { // jika tidak disimpan, textarea direfresh dan file global direset.
                TextArea.setText("");
                fileGlobal = "";
            }
        }
    }//GEN-LAST:event_NewActionPerformed

