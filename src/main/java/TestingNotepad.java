    private void OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenActionPerformed
        // TODO add your handling code here:
        String dir;
        if (!"".equals(TextArea.getText())) { //jika ada isinya, maka muncul prompt.
            int konfirmasi = JOptionPane.showConfirmDialog(null, "Simpan filenya?", "Konfirmasi Open File", JOptionPane.YES_NO_OPTION);
            if (konfirmasi == JOptionPane.YES_OPTION) { // jika disimpan, maka akan dilakukan proses save.
                if (!"".equals(fileGlobal)) {
                    dir = fileGlobal;
                } else {
                    jFileChooser1.showSaveDialog(null);
                    dir = jFileChooser1.getSelectedFile().getPath();
                    fileGlobal = dir;
                }
                String data = TextArea.getText();
                saveEdit(dir, data);
            } else { // jika tidak disimpan, maka proses open dikalukan.
                jFileChooser1.showOpenDialog(null);
                dir = jFileChooser1.getSelectedFile().getPath();
                fileGlobal = dir;
                TextArea.setText(openFile(dir));
            }
        } else { //jika tidak ada isinya, maka langsung dilakukan proses open.
            jFileChooser1.showOpenDialog(null);
            dir = jFileChooser1.getSelectedFile().getPath();
            fileGlobal = dir;
            TextArea.setText(openFile(dir));
        }
    }//GEN-LAST:event_OpenActionPerformed

